package com.elex.chatservice.model.mail.battle;

public class DragonReportParams
{
	private String dragonSkillId;
	private int dragonTimes;
	private int dragonFround;
	public String getDragonSkillId()
	{
		return dragonSkillId;
	}
	public void setDragonSkillId(String dragonSkillId)
	{
		this.dragonSkillId = dragonSkillId;
	}
	public int getDragonTimes()
	{
		return dragonTimes;
	}
	public void setDragonTimes(int dragonTimes)
	{
		this.dragonTimes = dragonTimes;
	}
	public int getDragonFround()
	{
		return dragonFround;
	}
	public void setDragonFround(int dragonFround)
	{
		this.dragonFround = dragonFround;
	}
	
	
}
